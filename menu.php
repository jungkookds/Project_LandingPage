    <h5 class="card-title">Venus</h5>
    <p class="card-text">
Venus adalah planet kedua dari Matahari dalam tata surya kita. Planet ini dikenal sebagai "kembar Bumi" karena memiliki ukuran yang mirip, tetapi kondisi permukaannya sangat berbeda. Venus memiliki atmosfer tebal dengan efek rumah kaca yang kuat, sehingga suhu di permukaannya sangat panas, mencapai sekitar 467 derajat Celsius.</p>
