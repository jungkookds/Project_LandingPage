    <h5 class="card-title">Bumi</h5>
    <p class="card-text">
Bumi adalah planet ketiga dari Matahari dan satu-satunya planet yang diketahui memiliki kehidupan. Planet ini memiliki atmosfer yang mendukung kehidupan, air dalam bentuk cair, suhu yang mendukung, serta keragaman ekosistem dan kehidupan. Bumi memiliki empat musim, rotasi sekitar sumbunya, dan memiliki satelit alam yang disebut Bulan. Itu adalah tempat satu-satunya di tata surya yang kita tahu memiliki kehidupan.</p>
