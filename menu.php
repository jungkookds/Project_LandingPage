    <h5 class="card-title">Merkurius</h5>
    <p class="card-text">
Merkurius adalah planet terkecil dan terdekat dengan Matahari. Dengan permukaan yang penuh kawah, suhu ekstrem, dan atmosfer tipis, ia sangat berbeda dari Bumi. Periode orbitnya sekitar 88 hari, sedangkan rotasinya sangat lambat. Misi seperti Mariner 10 dan Messenger telah mempelajari planet ini. Kehidupan tidak mungkin ada di sana karena kondisi yang ekstrem.</p>
