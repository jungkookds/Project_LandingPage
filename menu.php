  <h2>Tentang Kami</h2>
    <div class="row">
      <div class="col-5">
      Tata surya kita memiliki delapan planet yang terkenal, yaitu Merkurius, Venus, Bumi, Mars, Jupiter, Saturnus, Uranus, dan Neptunus. Setiap planet memiliki karakteristik uniknya sendiri, termasuk suhu, permukaan, dan komposisi atmosfer. Penelitian tentang planet sangat penting dalam pemahaman kita tentang tata surya dan juga dalam mencari tanda-tanda kehidupan di luar Bumi.<br>
      Atmosfer: Mayoritas planet memiliki atmosfer yang melingkupi permukaannya. Atmosfer ini terdiri dari berbagai gas dan berperan penting dalam menentukan cuaca dan iklim planet.

Satelit alami: Banyak planet memiliki satelit alami, yang juga dikenal sebagai bulan. Bulan-bulan ini mengorbit planet induk mereka.

Rotasi: Planet-planet berputar atau berotasi pada sumbunya sendiri. Ini memengaruhi panjang hari dan malam di planet tersebut.
